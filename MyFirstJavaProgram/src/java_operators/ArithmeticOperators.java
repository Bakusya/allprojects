package java_operators;

public class ArithmeticOperators {
    public static void main(String[] args){

        // Arithmetic Operators:

         int a = 10;
         int b = 5;

         //addition
        System.out.println(a+b);  //15

        //subtraction
        System.out.println(a-b);  //5

        //multiplication
        System.out.println(a*b);  //50

        //division
        System.out.println(a/b);  //2

        //modulo ---> modulus
        System.out.println(a%b);  //0

        System.out.println(13%2); //1

        int priceOfStrawberry = 500;
        int weight = 2;
        int result = priceOfStrawberry * weight;
        System.out.println("Price of strawberry: \t\t" + priceOfStrawberry +
                "\nWeight: \t\t\t\t\t" + weight +
                "\ntotal: \t\t\t\t\t\t" + result );

        int y = 19;
        int z = 20;
        int sumYZ =(y+z) * 54;
        System.out.println(sumYZ);
























    }




}
